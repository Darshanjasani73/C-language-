#include <iostream>
using namespace std;

int main()
{
    // Printing integers representing characters 'a' to 'z'
    cout << "Integers representing characters 'a' to 'z':" << endl;
    for (int i = 'a'; i <= 'z'; ++i)
    {
        cout << i << endl;
    }

    // Printing characters 'a' to 'z'
    cout << "Characters 'a' to 'z':" << endl;
    for (char j = 'a'; j <= 'z'; ++j)
    {
        cout << j << endl;
    }

    return 0;
}
